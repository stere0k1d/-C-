Тема: //Конвертация данных
    Конвертация данных — преобразование данных из одного формата в другой.

При вводе данных в консоль они представлены типом string.

Console.WriteLine("Введите число:"); // Переменная number будет иметь тип данных string, несмотря на то что в консоль мы введем число.
string number = Console.ReadLine(); // Это будет выглядеть похожим образом: string number = "34";

Представим, что нам нужно использовать число 34, например, для сложения, для этого нам надо переменную number с типом string конвертировать в числовой тип данных. Чтобы совершить данную операцию, существуют специальные методы.


    Метод //Parse
Для конвертации данных из строки можно использовать метод Parse
    
    int.Parse("34"); // Здесь int - тип, в который мы хотим преобразовать, а "34" - строковое значение числа.

Если мы захотим преобразовать в другой тип, надо использовать его метод Parse()

    double.Parse("3.14");
    byte.Parse("128");

Для использования результата конвертации его лучше сохранить в переменную:

    int a = int.Parse("34"); //Таким образом при помощи метода Parse мы можем преобразовывать строку в соответствующий тип. 

//Запомни:
Благодаря методу Parse можно конвертировать данные только из формата string.


// Также при помощи Parse можно сразу конвертировать данные, которые ввел пользователь, для этого надо метод Console.ReadLine() сделать аргументом метода Parse.

int a = int.Parse(Console.ReadLine());


Класс //Convert

Для конвертации данных из любого типа в любой можно использовать класс Convert

Convert.ToInt32("34"); //Здесь Int32 -название типа в библиотеке .NET (согласно таблице), в который мы хотим преобразовать, а "34" - строковое значение числа.

Если мы захотим преобразовать в другой тип, надо использовать его название в библиотеке .NET.

Convert.ToChar("!");
Convert.ToBoolean("True");


Для использования результата конвертации его лучше сохранить в переменную:

string a = Convert.ToString('!');


Таким образом при помощи класса Convert мы можем преобразовывать любые типы данных. 




