Тема: //Инкремент и декремент.Постфиксное и префиксное написание

Инкремент – это //увеличение переменной на единицу.

int a = 5;
a++; //a равно 6


Декремент – это //уменьшение переменной на единицу.

int a = 5;
a--; //a равно 4


Постфиксное написание -это когда //оператор стоит после переменной.

a++;
a--;

int a = 10;
Console.WriteLine(a++); //10
Console.WriteLine(a); //11


Префиксное написание -это когда //оператор стоит перед переменной.

--a;
++a;

int a = 10;
Console.WriteLine(++a); //11
Console.WriteLine(a); //11




